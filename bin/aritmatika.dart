void main() {
//pertambahan +
  var resultTambah = 10 + 10;

  print(resultTambah);

//pengurangan -
  var resultKurang = 10 - 5;
  print(resultKurang);

//perkalian *
  var resultKali = 5 * 5;
  print(resultKali);

//pembagian bulat /
  var resultBagi1 = 20 / 2;
  print(resultBagi1);

//pembagian decimal ~/
  var resultBagi2 = 20 / 2;
  print(resultBagi2);

//sisa bagi %
  var resultBagi3 = 10 % 3;
  print(resultBagi3);

  var first = 30;
  var second = 5;

  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);
}
